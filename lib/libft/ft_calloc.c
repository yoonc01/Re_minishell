#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void		*arr;
	size_t		mul_size_cnt;

	mul_size_cnt = count * size;
	if (count != 0 && mul_size_cnt / count != size)
		return (NULL);
	arr = malloc(count * size);
	if (!arr)
		return (NULL);
	ft_bzero(arr, count * size);
	return (arr);
}
